				<h3 class="text-center">Reseña de la Empresa</h3>
				<p class="text-justify">
					&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;La Escuela Educación Inicial Colegio Teresa de la Parra nace el 23 de Agosto de 2010, fundada por la Economista Maritza Coromoto Reverol, inicialmente funcionando solo como maternal, denominándose “Guardería Teresa de la Parra ‘Memorias de mamá Blanca’ ”, así ejerce con sede en Patiecitos en la quinta “San Judas Tadeo”, Municipio Guasimos durante dos años escolares. 
					
				</p>
				<p class="text-justify">
					&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;En Agosto de 2012 la institución cambia de propietario, siendo la Licenciada Sandra Aldana de Moreno y la Licenciada María Carrillo de Peña sus nuevas gerentes. Bajo esta administración cambia de sede, Calle 6 entre Carreras 3 y 4 N°3-40 en Táriba,
					funcionando con los 2 niveles de Educación Inicial: Maternal y Pre-escolar. Despúes de 2 años la institución vuelve a cambiar de sede, una casa de estilo colonial ubicada en la misma calle, la cual es la sede actual del colegio.
				</p>
